local function open_split_term(cmd)
  -- Open a horizontal split and create a terminal
  vim.cmd('split') -- Open a new horizontal split
  vim.cmd('resize 15') -- Resize the split window to be 15 lines high
  local buf = vim.api.nvim_create_buf(false, true) -- Create a new buffer
  vim.api.nvim_set_current_buf(buf) -- Set the split to use the new buffer
  vim.fn.termopen(cmd) -- Run the terminal command in the split window
  vim.cmd('startinsert') -- Enter insert mode to interact with the terminal
end

-- Compile the current sketch in a split terminal
vim.keymap.set('n', '<leader>ac', function()
  open_split_term('arduino-cli compile --fqbn arduino:avr:uno "' .. vim.fn.expand('%:p') .. '"')
end, { desc = '[A]rduino [C]ompile' })

-- Upload the current sketch in a split terminal
vim.keymap.set('n', '<leader>au', function()
  open_split_term('arduino-cli upload -p /dev/ttyACM* --fqbn arduino:avr:uno "' .. vim.fn.expand('%:p:h') .. '"')
end, { desc = '[A]rduino [U]pload' })


